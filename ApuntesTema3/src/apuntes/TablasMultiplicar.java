package apuntes;

public class TablasMultiplicar {

	public static void main(String[] args) {

		for(int i=1; i<=10; i++) {
			System.out.printf("Tabla del %d \n", i);
			for(int j=1; j<=10; j++) {
				System.out.printf("%d X %d = %d \n", i, j, i*j);
			}
			System.out.println("-----------------");
		}
			
			
			
			
			
			
			
			
			
			
			

	}

}
